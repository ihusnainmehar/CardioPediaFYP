package com.example.cardiopediafyp;

public class ChildItem {
    private String title;
    private int logo;

    public ChildItem(String title, int logo) {
        this.title = title;
        this.logo = logo;
    }

    public String getTitle() {
        return title;
    }

    public int getLogo() {
        return logo;
    }
}
